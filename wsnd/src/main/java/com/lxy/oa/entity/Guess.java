package com.lxy.oa.entity;

public class Guess {
    private int guessid;
    private int money;
    private int mark;

    public int getGuessid() {
        return guessid;
    }

    public void setGuessid(int guessid) {
        this.guessid = guessid;
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    private int userid;

}
